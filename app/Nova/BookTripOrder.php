        'id',
    ];

    /**
     * Get the fields displayed by the resource.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return array
     */
    public function fields(Request $request)
    {
        return [
            ID::make(__('ID'), 'id')->sortable(),
            BelongsTo::make('Flight Order', 'FlightOrder', \App\Nova\BookFlightOrder::class),
            Boolean::make('processed'),
            Boolean::make('processing'),
            Boolean::make('requires_attention'),
            Boolean::make('paid'),
            Boolean::make('paid_with_klarna')->onlyOnDetail(),
            Boolean::make('paid_with_stripe')->onlyOnDetail(),

        ];
    }

    /**
